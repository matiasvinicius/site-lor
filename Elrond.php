					<p>Elrond era o Senhor de Rivendell, um dos poderosos governantes que permaneceram na Terra-média durante a Terceira Era. Seu nome foi explicado por Tolkien em uma carta de 1958 a Rhona Beare[1] como "Elfo da Caverna", já que ele foi encontrado ainda criança abandonado em uma caverna. Notas posteriores, refletidas em O Silmarillion e A Guerra das Jóias, por sua vez interpretam o nome como "Cúpula da Estrela" ou "Abóbada de Estrelas" (a abóbada no sentido da cúpula celestial). </p>

					<p>Elrond era o filho de Eärendil e Elwing, e bisneto de Lúthien, nascido em Beleriand na Primeira Era, fazendo com que tivesse mais de 6000 anos de idade na época dos eventos descritos em O Senhor dos Anéis. Seu irmão gêmeo era Elros Tar-Minyatur, o primeiro Alto Rei de Númenor. Embora seja considerado um meio-elfo, não foi concebido uma avaliação percentual exata. Através de Lúthien, filha de Melian, a Maia, ele e seu irmão Elros também eram descendentes dos Maiar, seres angélicos que tinham vindo à Terra Média milhares de anos antes. Elrond, junto com seus pais, seu irmão, e seus filhos, foram concedidos a escolha entre o destino dos elfos ou humanos pelos Valar. Elrond escolheu viajar para o Oeste e viver como um elfo imortal, enquanto seu irmão gêmeo Elros escolheu a mortalidade. </p>
					
