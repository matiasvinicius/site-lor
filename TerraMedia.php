					<img src = "http://tolkienbrasil.com/wp-content/uploads/2015/05/Imrahil-dol-amroth-map.jpg" width="400" height="300" align="left"alt="Mapa da Terra Média" title="Mapa da Terra Média">

					<p>Terra Média é o nome dado para a terra antiga e fictícia de J. R. R. Tolkien, onde a maioria dos contos do seu imaginário ocorrem. Terra Média é a tradução literal do termo anglo-saxão middangeard, referindo-se a este mundo, o reino dos humanos. Tolkien traduziu "Terra-média" como Endor (algumas vezes Endórë) e Ennor nas línguas élficas Quenya e Sindarin.</p>
					
					<p>Apesar de o cenário da Terra Média ser geralmente considerado outro mundo, é na realidade um período imaginário do passado da nossa própria Terra. Tolkien insistiu que a Terra Média é a nossa Terra em várias das suas cartas, sendo que na carta 211 ele estima que a Terceira Era teria terminado 600 mil anos antes do nosso próprio tempo. A ação nos livros é bastante restrita ao noroeste do continente, correspondendo à atual Europa, e pouco é conhecido sobre o leste e sul da Terra Média. A história da Terra Média está dividida em várias Eras: O Hobbit e O Senhor dos Anéis lidam exclusivamente com eventos relacionados com o fim da Terceira Era, enquanto que O Silmarillion trata principalmente da Primeira Era. O seu mundo era originalmente plano mas foi tornado redondo perto do fim da Segunda Era por Eru Ilúvatar, o Criador.</p>
					
					<p>Muito do nosso conhecimento da Terra Média é baseado em escritos que Tolkien não acabou para publicação durante a sua vida. Nestes casos, este artigo é baseado na versão do imaginário que é considerado canônico pela maioria de fãs de Tolkien, tal como discutido no canôn da Terra Média. </p>
					
