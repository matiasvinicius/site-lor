					<img src = "https://pmcdeadline2.files.wordpress.com/2015/12/gollum.jpg?w=446&h=299&crop=1" width = "300px" height = "200px" align = "left" alt = "Gollum" title = "Gollum">
					
					<p>Gollum é um personagem fictício das obras do filólogo e professor britânico J. R. R. Tolkien. Ele foi introduzido na obra de fantasia O Hobbit de 1937, e se tornou um importante personagem na sequência O Senhor dos Anéis. Gollum era um hobbit cascalvas, do povo do rio, que viveu perto dos Campos de Lis. Originalmente conhecido como Sméagol, ele foi corrompido por Um Anel e, mais tarde foi chamado de Gollum após o seu hábito de fazer "barulhos horríveis ao engolir sua garganta".</p>
					
					<img src = "https://vignette.wikia.nocookie.net/lotr/images/5/54/SmeagolAB.jpg/revision/latest?cb=20101001185856" width = "150px" height = "150px" align = "right" alt = "Sméagol" title = "Sméagol">
					
					<p>Sméagol, como era antigamente chamado, vivia nos Campos de Lis. O povo de Sméagol vivia nos Vales do Anduin perto dos Campos de Lis. Eram descendentes da linhagem hobbit dos Grados, que migraram para lá vindos de Eriador, por volta de 1356. Os Grados e os Hobbits mantinham hábitos e feições comuns; mas os Grados eram pouco mais primitivos que os parentes distantes, estabelecidos no Condado. Sméagol provavelmente nasceu na primeira metade do século.Era membro de uma grande, próspera e respeitada família, que tinha sua avó como matriarca. No dia do seu aniversário, por volta de 2463 da Terceira Era, Sméagol foi pescar com seu primo Déagol. Déagol foi puxado para a água por um peixe, e emergiu com um anel de ouro. Ambos contemplavam o Um Anel, pertencente a Sauron, o Senhor das Trevas, e perdido durante a morte de Isildur na batalha dos Campos de Lis muito tempo antes. O desejo de Sméagol pelo Anel levou-o a matar Déagol. Ele escondeu o corpo e ninguém jamais descobriu o que aconteceu com Déagol. Sméagol descobriu os poderes do Um Anel ainda sob o teto de sua avó, usando-o para descobrir segredos, cometer pequenos furtos e toda a sorte de malfeitos. Com o tempo, os atos de Sméagol começaram a refletir em sua família, que o desprezava cada vez mais. Sua garganta começou a fazer um barulho muito característico, o que lhe rendeu o apelido "Gollum". Finalmente, a avó de Sméagol o expulsou de seu lar. Sméagol vagou ao norte pelas bordas do Anduin, sem destino. Seu corpo sofreu alterações significativas. Com o tempo, começou a odiar a luz do Sol, buscando lugares cada vez mais escuros. </p>
					
