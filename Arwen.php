					<img src = "https://vignette.wikia.nocookie.net/lotr/images/d/dd/100_beautiful_arwen.jpg/revision/latest/scale-to-width-down/350?cb=20110313201140" width = "300px" height = "400px" align = "left" alt = "Arwen" title = "Arwen">
					
					<p>Arwen é a filha mais nova de Elrond e Celebrían. Seus irmãos mais velhos são os gêmeos Elladan e Elrohir. Seu nome Ar-wen significa 'nobre donzela'. Ela concebeu a alcunha de "Estrela Vespertina" (ou Estrela da Noite), como a mais bonita da última geração dos Altos Elfos na Terra Média. Como dito em O Conto de Aragorn e Arwen, Aragorn em seu vigésimo ano conheceu Arwen pela primeira vez em Rivendell, onde vivia sob a proteção de Elrond. Arwen, após mais de 2700 anos, que recentemente voltou para a casa de seu pai, depois de viver por um tempo com a avó Senhora Galadriel em Lórien. Aragorn se apaixonou por ela à primeira vista. Cerca de trinta anos depois, os dois se reencontraram em Lórien. Retribuiu o amor de Aragorn, e no monte de Cerin Amroth comprometeram-se a casar com o outro. </p>
					
					<p>Arwen aparece pela primeira vez nos textos de O Senhor dos Anéis em Rivendell logo após Frodo acordar na Casa de Elrond. Ela sentou-se ao lado de seu pai na festa de comemoração. Quando a Sociedade do Anel veio a Lothlórien, Aragorn lembrou sua reunião anterior e fez uma pausa em reverência a Cerin Amroth. Pouco antes de Aragorn tomar as Sendas dos Mortos, ele foi acompanhado por um grupo de Dúnedain do Norte, acompanhado pelos irmãos de Arwen, Elladan e Elrohir, que lhe trouxe como presente de sua irmã uma bandeira de pano preto. A bandeira foi desfraldada na Batalha dos Campos de Pelennor para revelar o emblema de Elendil figurada em mithril, pedras preciosas e ouro; este tornou-se o primeiro anúncio público triunfante do retorno do rei. </p>
					
					<p>Após a Guerra do Anel, Aragorn se tornou rei de Arnor e Gondor. Arwen chegou a Minas Tirith, e eles se casaram. Ela deu a Frodo um colar com uma pedra branca, o que lhe ajudou quando a escuridão de seus ferimentos o perturbassem. Suas poucas aparições nos livros sugerem que Arwen é uma personagem secundária em O Senhor dos Anéis; mas ela serve de inspiração e motivação para Aragorn, que deve tornar-se Rei de ambos os reinos antes que Elrond permitisse que ela se casasse com ele. O Conto de Aragorn e Arwen relata que ambos tiveram um filho, Eldarion, e pelo menos duas filhas sem nome. Em 121 da Quarta Era, um ano após a morte de Aragorn, Arwen desistiu de sua vida, com 2901 anos de idade, em Cerin Amroth. </p>
					
