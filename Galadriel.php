					<p>Galadriel é uma personagem criada por J.R.R. Tolkien, em seu legendárium da Terra Média. Ela aparece em O Senhor dos Anéis, O Silmarillion e Contos Inacabados.</p>
					
					<p>Era uma elfa pertencente à realeza, tanto dos Noldor como dos Teleri, sendo neta de ambos os reis Finwë e Olwë, sendo também parente próxima do rei Ingwë de Vanyar através de sua avó Indis. Ela foi uma dos líderes da rebelião dos Noldor e sua fuga de Valinor durante a Primeira Era; ela foi a única Noldor proeminente a retornar no final da Terceira Era. Até o fim de sua estada na Terra média, era co-governante de Lothlórien com o marido, Senhor Celeborn, e foi referida várias vezes como a Senhora de Lórien, Senhora dos Galadhrim, Senhora da Luz, ou Senhora da Floresta Dourada. Sua filha Celebrían era a esposa de Elrond e mãe de Arwen, Elladan e Elrohir.</p>
					
					<p>Tolkien descreve Galadriel como "a mais poderosa e mais bela de todos os elfos que permaneceram na Terra média " (após a morte de Gil-galad ) e "a maior das mulheres élficas".</p>
					
