					<p>Nascido no ano 2746 da Terceira Era, Thorin foi para o exílio após a tomada de Erebor pelo dragão Smaug em 2770, juntamente com os outros anões sobreviventes. Na Batalha de Azanulbizar em 2799, quando tinha apenas 53 anos (uma idade nova para um anão), marchou com um exército anão em Nanduhirion abaixo do Portão-Leste de Moria. O escudo de Thorin quebrou-se durante a batalha, e este passou a usar apenas um tronco de carvalho como defesa, assim ganhando o apelido ou sobrenome "Oakenshield" que significa Escudo de Carvalho.</p>
					
