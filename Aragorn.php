					<p>Aragorn II, filho de Arathorn é uma personagem fictícia criada pelo professor e filólogo britânico J. R. R. Tolkien na sua obra O Senhor dos Anéis, onde é um dos protagonistas. Aragorn era um Guardião do Norte, introduzido pela primeira vez com o nome Strider[nota 1] em Bree, como os hobbits continuaram a chamá-lo ao longo de O Senhor dos Anéis. Acabou sendo descoberto como herdeiro de Isildur e pretendente legítimo aos tronos de Arnor e Gondor. Também era um confidente de Gandalf e parte integrante da missão para destruir o Um Anel e derrotar o Senhor das Trevas Sauron.</p>
					
