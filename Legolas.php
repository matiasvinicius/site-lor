					<p>Legolas Greenleaf (nos países lusofonos conhecido como Legolas Verdefolha ou Légolas Folha Verde), dentro do universo de fantasia criado pelo escritor J.R.R. Tolkien, foi um elfo Sindar, filho de Thranduil, rei da Floresta das Trevas. Em 3019 da Terceira Era, Legolas foi a Valfenda como mensageiro dos elfos de sua terra. Lá participou do Conselho de Elrond, onde se juntou à Sociedade do Anel representando toda a raça élfica. É considerado um dos mais misteriosos personagens de Tolkien. No filme de Peter Jackson o personagem foi representado por Orlando Bloom. </p>
					
					<p>O "primeiro Legolas" a aparecer na obra de Tolkien não foi o retratado na obra de O Senhor dos Anéis e acima referido. O primeiro elfo denominado "Legolas Verdefolha" foi um elfo de ascendência Noldor (apesar de alguns acreditarem que na verdade possuí ascendência sindarin, uma vez que o autor, Tolkien, nunca deixou claro tal detalhe) habitante de cidade élfica de Gondolin, que viveu durante a Primeira Era, aparecendo no livro O Silmarillion. No conto em que o personagem aparece, denominado "The Fall of Gondolin" (em português, "A Queda de Gondolin"), ele é mencionado apenas uma única vez, guiando os elfos sobreviventes à destruição da cidade para um local seguro. </p>
						
