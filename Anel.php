					<p>Os Anéis de Poder foram forjados por Celebrimbor e seus companheiros, guiados por Sauron. Três Anéis foram dados aos Reis-Elfos, Sete aos senhores das Casas dos Anões e Nove Anéis foram dados à raça dos homens. O Senhor do Escuro, porém, criou em segredo um outro anel, o Um, na Montanha da Perdição em Mordor, para controlar o poder dos demais anéis forjados pelos elfos. </p>
					
					<p>Durante as histórias de Tolkien, o Anel possuía vontade própria e sempre era atraído pela vontade de seu criador. O portador do Anel, não importasse sua índole, era atraído pelo seu poder e poderia até matar para ficar com ele (como Sméagol matou seu primo Déagol para ficar com o anel). Seu poder mais notório era o de deixar seu usuário invisível(na realidade o transportava para o mundo espectral), e possuía a propriedade de aumentar ou diminuir de tamanho e peso conforme o portador e a carga de energia negativa presente, também tinha controle absoluto sobre o próprio tempo e espaço, mas esse uso era apenas acessível para Sauron (ou para Gandalf, Elrond, Galadriel, Saruman, Radagast se tivessem o anel) e podia controlar a mente e a vontade de qualquer ser vivo ou da própria Natureza. Era indestrutível, exceto pelo fogo imortal da Montanha da Perdição, onde havia sido forjado.</p>
					
