					<p>O Silmarillion (The Silmarillion, no original) é uma coletânea de obras literárias de mito-poesias de J. R. R. Tolkien. Foi editada e publicada postumamente em 1977 por seu filho Christopher Tolkien com a ajuda de Guy Gavriel Kay, que, mais tarde, tornou-se um notável escritor de fantasia. O Silmarillion, juntamente com outras obras de Tolkien, forma uma extensa, embora incompleta, narrativa que descreve o universo de Eä, onde se encontram as terras de Valinor, Beleriand, Númenor e da Terra Média, onde se passam O Hobbit e O Senhor dos Anéis.</p>
					<p>Depois do sucesso de O Hobbit, e antes da publicação de O Senhor dos Anéis, a editora de Tolkien solicitou uma continuação de O Hobbit. Tolkien enviou um rascunho de O Silmarillion, mas, por conta de um mal-entendido, o editor rejeitou o projeto sem lê-lo na íntegra. Como resultado, Tolkien começou a trabalhar em "Uma jornada inesperada", o primeiro capítulo do que ele descreveu na época como "uma nova história sobre hobbits", que se tornou O Senhor dos Anéis.</p>
					
					<p>O Silmarillion compreende cinco partes: a primeira, Ainulindalë, fala da criação de Eä, o "mundo que é"; a segunda, Valaquenta, dá uma descrição dos Valar e Maiar, os poderes sobrenaturais de Eä; a terceira, Quenta Silmarillion, que forma a maior parte da coleção, narra a história dos eventos antes e durante a Primeira Era, incluindo as guerras pelas Silmarils que deu título ao livro; a quarta, Akallabêth, relata a história da Queda de Númenor e seu povo, que tem lugar na Segunda Era; e a quinta, Dos Anéis de Poder e da Terceira Era, é um breve relato das circunstâncias que levaram e foram apresentadas em O Senhor dos Anéis. As cinco partes eram, inicialmente, trabalhos separados, mas era desejo expresso de Tolkien que fossem publicados conjuntamente.[1] Dada sua morte antes de terminar a revisão das várias lendas, seu filho Christopher reuniu materiais a partir dos escritos mais antigos de seu pai para preencher o livro. Em alguns casos, isso significou que ele tinha de elaborar material completamente novo, a fim de resolver as lacunas e inconsistências na narrativa. </p>
					
